grade = int(input("Enter your grade: "))
if grade >= 80:
    print("You passed the test.")
elif grade >= 70:
    print("You have a B.")
elif grade >= 60:
    print("You have a C.")
elif grade >= 50:
    print("You have a D.")
else:
    print("You have Failed the test F.")
